# en python se pueden crear multiples variables en una sola linea, de la siguiente manera:

x, y, z = 5, 10, 15

# en este caso, x va a ser igual a 5, y va a ser igual a 10 y z va a ser igual a 15

# se pueden asignar multiples valores a una sola variable, de la siguiente manera:

x = y = z = 5

# en este caso, x, y y z van a ser iguales a 5

# se pueden asignar multiples valores a multiples variables, de la siguiente manera:

x, y, z = 5, 10, 15

# en este caso, x va a ser igual a 5, y va a ser igual a 10 y z va a ser igual a 15

