# Tipos de datos
# Los tipos de datos son las características que tienen los valores que se pueden almacenar en una variable.
# En Python, los tipos de datos son:

## 
# int: Números enteros = 10
# float: Números decimales = 10.5
# str: Cadenas de texto = Hola, mundo!'
# bool: Booleanos (True o False)
# list: Listas = [1, 2, 3]
# tuple: Tuplas = (1, 2, 3)
# dict: Diccionarios = {'nombre': 'Juan', 'edad': 25}
# set: Conjuntos = {1, 2, 3}
##

# int (enteros) 
numero_entero = 10

# float (decimales)
numero_decimal = 10.5

# str (cadenas de texto)
cadena_texto = 'Hola, mundo!'

# bool (booleanos)
booleano = True

# list (listas)
lista = [1, 2, 3]

# tuple (tuplas)
tupla = (1, 2, 3)

# dict (diccionarios)
diccionario = {'nombre': 'Juan', 'edad': 25}

# set (conjuntos)
conjunto = {1, 2, 3}    


