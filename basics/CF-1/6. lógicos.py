# los operadores logicos son los siguientes:
#
# and: este operador devuelve True si ambos operandos son True
# or: este operador devuelve True si al menos uno de los operandos es True
# not: este operador devuelve True si el operando es False
#

# ejemplo de operadores logicos
x = True
y = False

# and
print(x and y)

# or
print(x or y)

# not
print(not x)

# en este caso, la salida de los print va a ser False, True, False
# en el primer print, x es True y y es False, por lo tanto, la salida es False
# en el segundo print, x es True y y es False, por lo tanto, la salida es True
# en el tercer print, x es True, por lo tanto, la salida es False