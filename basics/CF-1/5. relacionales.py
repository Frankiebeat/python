# los operadores relacionales son los siguientes:

# == igual
# != diferente
# > mayor que
# < menor que
# >= mayor o igual que
# <= menor o igual que

# ejemplo de operadores relacionales
x = 5
y = 10
z = 5

# igual
print(x == y)

# diferente
print(x != y)

# mayor que
print(x > y)

# menor que
print(x < y)

# mayor o igual que
print(x >= z)

# menor o igual que
print(x <= z)
# en este caso, la salida de los print va a ser False, True, False, True, True, True
